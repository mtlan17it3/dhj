package com.example.my2.doituong;

public class Hinhanh {

    private String Ten;
    private int Hinh;

    public Hinhanh(String ten, int hinh) {
        Ten = ten;
        Hinh = hinh;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }
}

